    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = this.nome.getText();
        Funcionarios f = new Funcionarios();
        if (name.length() == 0) {
            JOptionPane.showMessageDialog(this, "VocÃª deve informar o nome da aparelho!");
            return;
        }
        String v = jComboBox1.getSelectedItem().toString();
        System.out.println("Aqui" + v);
        f.setNome(name);
        f.setCpf(this.cpf.getText());
        if (v.equals("Masculino")) {
            f.setSexo("1");
        }else{
            f.setSexo("0");
        }
        f.setIdSetor(6);
        
        try {
            f.cadastrarFuncionario(f);
        } catch (Exception ex) {
            Logger.getLogger(Funcionario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            DefaultTableModel val = (DefaultTableModel) List.getModel();
            val.getDataVector().removeAllElements();
            List<Funcionarios> marca = f.buscarTodos();
            for (int i = 0; i < marca.size(); i++) {
                String a = marca.get(i).getSexo();
                String sexo = "";
                if(a.equals("1"))
                {
                    sexo = "Masculino";
                }else
                {
                    sexo = "Feminino";
                }
                val.addRow(new String[]{"" + marca.get(i).getId() + "", marca.get(i).getNome()  + "", marca.get(i).getCpf(),sexo});
            }
        } catch (Exception ex) {
            Logger.getLogger(MarcasView.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

