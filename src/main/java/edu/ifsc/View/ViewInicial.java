    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String arqRel = "C:\\Users\\rafac\\JaspersoftWorkspace\\MyReports\\";
        arqRel += "Simple_Blue.jasper";
        System.out.println(arqRel);
        try {
            DBController db = new DBController();
            JasperReport rel = (JasperReport) JRLoader.loadObjectFromFile(arqRel);
            db.conectar();
            JasperPrint imp = JasperFillManager.fillReport(rel, null, db.getConexao());
            db.desconectar();
            JasperViewer v = new JasperViewer(imp);
            v.setVisible(true);
        } catch (JRException ex) {
            System.out.println(ex.getMessage());
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

